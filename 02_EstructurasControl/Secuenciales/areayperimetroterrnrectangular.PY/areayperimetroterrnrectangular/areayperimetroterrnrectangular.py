# Calculando área y perímetro de un terreno rectangular
print("Calculando area y Perimetro de un Terreno Rectangular")

l1 = float(input("Ingrese primer lado del terreno: "))
l2 = float(input("Ingrese segundo lado del terreno: "))

area = l1 * l2
perimetro = 2*l1 + 2*l2

print(f"area: {area}")
print(f"Perimetro: {perimetro}")

